
        function openNav() {
            document.getElementById("mySidenav").style.width = "250px";
            document.getElementById("line_bars").style.display = 'none';
            document.getElementById("close_icon").style.display = 'block';

        }

        function closeNav() {
            document.getElementById("mySidenav").style.width = "0";
            document.getElementById("close_icon").style.display = 'none';
            document.getElementById("line_bars").style.display = 'block';
        }
